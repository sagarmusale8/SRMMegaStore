/*
 *  Extensions for all elements will be here
 */

import UIKit

// MARK: UIView
extension UIView{
    
    // Getting view width
    func viewWidth()->CGFloat{
        return self.frame.size.width
    }
    
    // Getting view width
    func viewHeight()->CGFloat{
        return self.frame.size.height
    }
}

